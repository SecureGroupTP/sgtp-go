package client

// Client определяет публичный интерфейс для взаимодействия с сессией SGTP.
//
// Типичный жизненный цикл:
//
//	// 1. Создание клиента
//	cfg := client.Config{
//	    ServerAddr: "relay.example.com:443",
//	    RoomUUID:   roomUUID,
//	    UUID:       myUUID,
//	    PrivateKey: myEd25519Key,
//	}
//	c, err := client.New(cfg)
//
//	// 2. Подключение к серверу
//	if err := c.Connect(); err != nil { /* handle error */ }
//
//	// 3. Запуск обработчиков событий и сообщений в отдельных горутинах
//	go func() {
//	    for msg := range c.Messages() {
//	        // обработка входящих сообщений
//	    }
//	}()
//	go func() {
//	    for event := range c.Events() {
//	        // обработка событий сессии (подключения, ротация ключей, ошибки)
//	    }
//	}()
//
//	// 4. Отправка сообщений (только после получения события EventChatKeyRotated)
//	_, err = c.SendMessage([]byte("hello"))
//
//	// 5. Корректное завершение
//	_ = c.Disconnect()
type IClient interface {
	// Messages возвращает канал для чтения расшифрованных и аутентифицированных
	// входящих сообщений группы.
	//
	// Важно: канал необходимо читать непрерывно. Если буфер переполнится,
	// новые сообщения будут отбрасываться. Канал закрывается при завершении
	// сессии.
	//
	// Возвращает: <-chan InboundMessage — read-only канал сообщений.
	Messages() <-chan InboundMessage

	// Events возвращает канал событий жизненного цикла сессии.
	//
	// Типы событий:
	//   • EventPeerConnected / EventPeerDisconnected — изменение состава участников
	//   • EventChatKeyRotated — получен новый групповой ключ шифрования
	//   • EventError — критическая ошибка, требующая внимания
	//   • EventKicked — клиент был исключён из комнаты
	//
	// Как и Messages(), этот канал нужно читать постоянно, чтобы избежать
	// переполнения буфера и потери событий.
	//
	// Возвращает: <-chan Event — read-only канал событий.
	Events() <-chan Event

	// Connect устанавливает TCP-соединение с реле-сервером, отправляет
	// frame намерения подключения (согласно §3 спецификации) и запускает
	// фоновую горутину для чтения входящих фреймов.
	//
	// Метод возвращает управление после успешного установления соединения
	// и отправки intent-фрейма. Фактическая аутентификация и обмен ключами
	// происходят асинхронно — отслеживайте события через Events().
	//
	// Возвращает: error при неудаче подключения или отправки intent-фрейма.
	Connect() error

	// SendMessage шифрует переданные данные с использованием текущего
	// группового ключа (Chat Key) и транслирует сообщение всем участникам комнаты.
	//
	// Требования:
	//   • Должно быть получено хотя бы одно событие EventChatKeyRotated
	//     (ключ ещё не установлен при старте)
	//   • Данные будут зашифрованы методом, определённым в protocol.Encrypt
	//
	// Возвращает:
	//   • [16]byte — уникальный идентификатор отправленного сообщения (UUID)
	//   • error — при ошибке шифрования, отправки или отсутствии активного ключа
	//
	// Пример:
	//
	//	// Дождаться ротации ключа
	//	<-c.Events() // EventChatKeyRotated
	//	msgUUID, err := c.SendMessage([]byte("секретное сообщение"))
	SendMessage(data []byte) ([16]byte, error)

	// SendFIN отправляет широковещательное уведомление о корректном
	// завершении работы клиента в комнате.
	//
	// Используется для информирования других участников о том, что клиент
	// намеренно покидает сессию (в отличие от внезапного обрыва соединения).
	//
	// Возвращает: error при неудаче отправки фрейма.
	//
	// Примечание: после SendFIN рекомендуется вызвать Disconnect() для
	// закрытия транспортного соединения.
	SendFIN() error

	// Disconnect инициирует корректное завершение сессии:
	//   1. Отправляет FIN-уведомление (если соединение активно)
	//   2. Закрывает каналы done и останавливает фоновые горутины
	//   3. Закрывает TCP-соединение
	//
	// Метод идемпотентен: повторные вызовы безопасны и не возвращают ошибку.
	//
	// Возвращает: error только при проблеме с закрытием соединения
	// (игнорируется, если клиент уже закрыт).
	Disconnect() error

	// KnownPeers возвращает снимок (snapshot) текущих известных пиров,
	// для которых установлен общий секретный ключ.
	//
	// Возвращаемая коллекция — копия внутренней карты: изменения в ней
	// не влияют на состояние клиента. Каждый элемент — копия структуры Peer,
	// поэтому безопасен для конкурентного использования.
	//
	// Возвращает: []*Peer — срез с текущими известными пирами.
	//
	// Примечание: список может устареть сразу после возврата — для
	// актуального состояния подписывайтесь на события подключения/отключения.
	KnownPeers() []*Peer

	// IsMaster определяет, должен ли данный клиент выполнять роль
	// «мастера» в комнате для координации ротации групповых ключей.
	//
	// Логика: мастером считается участник с наименьшим UUID среди всех
	// известных пиров (сравнение через protocol.UUIDLess).
	//
	// Возвращает: true, если текущий клиент — мастер; false в противном случае.
	//
	// Важно: результат актуален на момент вызова. При изменении состава
	// участников (подключения/отключения) статус мастера может измениться —
	// отслеживайте события для корректной реакции.
	IsMaster() bool
}
